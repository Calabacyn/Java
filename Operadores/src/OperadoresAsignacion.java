public class OperadoresAsignacion {
    public static void main(String[] args) {
        //Asignacion =
        var miNumero = 10;
        int miNumero2; //Declaracion
        miNumero2 = 5;

        //Asignacion compuesto
        // +=

        miNumero += 5;// miNumero = miNumero + 5
        System.out.println("miNumero = " + miNumero);
        // -=, *=, /=, >=, <=
        miNumero *= 2;
        System.out.println("miNumero = " + miNumero);

    }
}
